#include <string.h>

void	ft_bzero(void *s, size_t n)
{
	unsigned char		*hol;
	size_t				i;

	hol = (unsigned char*)s;
	i = 0;
	while (i < n)
	{
		hol[i] = 0;
		i++;
	}
}
